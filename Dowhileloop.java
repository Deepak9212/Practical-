package ControlStatement.java;

public class Dowhileloop {
	public static void main(String[] args) {
		int i=1;
		do {
			System.out.println(i);//statement
			i++;
		}while(i<=10);//condition (exit control loop}
		do {
			System.out.println("chalte rho");//statement
		}while(true);//defining do while loop
	}
}

