package ControlStatement.java;

public class Switcheg2 {
	public static void main(String[] args) {
		char s='S';
		switch(s) {
		case 'A':{
			System.out.println("A");
			break;
			
		}
		
		case'S':{
			System.out.println("Deepak");
			break;
			
		}
		case'E':{
			System.out.println("E");
			break;
		}
		default:System.out.println("Not match");
		}
		
	}

}
